#Calculadora de dois números
numero1 = float(input("Digite o primeiro nº: ")) #número é uma variável
numero2 = float(input("Digite o segundo nº: ")) #número é uma variável

soma = numero1 + numero2

print (f"A soma do {numero1} e do {numero2} é: {soma}") 

""" # o "f" simboliza que existe uma variável dentro da String """
